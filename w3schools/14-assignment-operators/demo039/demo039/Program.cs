// W3Schools, C# Operators, Assignment
using System;

namespace MyApplication
{
    class Program
    {
        static void Main(string[] args)
        {
            int x;
            
            // =
            x = 5;
            Console.WriteLine(x);

            // +=
            x += 3;
            Console.WriteLine(x);

            x = x + 3;
            Console.WriteLine(x);

            // -=
            x -= 3;
            Console.WriteLine(x);

            x = x - 3;
            Console.WriteLine(x);

            // *=
            x *= 3;
            Console.WriteLine(x);

            x = x * 3;
            Console.WriteLine(x);

            // /=
            x /= 3;
            Console.WriteLine(x);

            x = x / 3;
            Console.WriteLine(x);

            // %=
            x %= 3;
            Console.WriteLine(x);

            x = x % 3;
            Console.WriteLine(x);

            // &=
            x &= 3;
            Console.WriteLine(x);

            x = x & 3;
            Console.WriteLine(x);

            // |=
            x |= 3;
            Console.WriteLine(x);

            x = x | 3;
            Console.WriteLine(x);

            // ^=
            x ^= 3;
            Console.WriteLine(x);

            x = x ^ 3;
            Console.WriteLine(x);

            // >>=
            x >>= 3;
            Console.WriteLine(x);

            x = x >> 3;
            Console.WriteLine(x);

            // <<=
            x <<= 3;
            Console.WriteLine(x);

            x = x << 3;
            Console.WriteLine(x);
        }
    }
}