// W3Schools, C# Strings, Concatenation
using System;

namespace MyApplication
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 10;
            int y = 20;
            int z = x + y;
            Console.WriteLine(z);   // z will be 30 (an integer/number)

            string a = "10";
            string b = "20";
            string c = a + b;
            Console.WriteLine(c);  // c will be 1020 (a string)
        }
    }
}