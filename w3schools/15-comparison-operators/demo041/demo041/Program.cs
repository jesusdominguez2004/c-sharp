// W3Schools, C# Operators, Comparison
using System;

namespace MyApplication
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 5;
            int y = 3;
            Console.WriteLine(x > y);   // returns True
            Console.WriteLine(x == y);  // returns False
            Console.WriteLine(x != y);  // returns True
            Console.WriteLine(x > y);   // returns True
            Console.WriteLine(x < y);   // returns False
            Console.WriteLine(x >= y);  // returns True
            Console.WriteLine(x <= y);  // returns False
        }
    }
}